    public VentanaProductosAZ() {
        initComponents();
        this.setLocationRelativeTo(null);
        RSAnimation.setBajar(-50, 200, 2, 2, this);
        mod.setColumnIdentifiers(new Object[]{"Nombre","Fecha de vencimiento","Tipo de producto"});
        TablaDeMuestra.setModel(mod);
        mod.setRowCount(0);
        List <ProductoCongelado> PCongelados = new ArrayList<>();
        List <ProductoFresco> PFrescos = new ArrayList<>();
        List <ProductoRefrigerado> PRefrigerados = new ArrayList<>();
        List <Producto> Productos = new ArrayList();
        try{
            
            UIManager.setLookAndFeel("com.sun.java.swing.plaf.windows.WindowsLookAndFeel");
            
        }
        catch (Exception e){
            
        }
        try{
            
            File archivo = new File("D:/Escritorio/Proyecto-Final/src/archivos/productosCongelados.txt");
            FileReader reader = new FileReader(archivo);
            BufferedReader breader = new BufferedReader(reader);
            if(archivo.exists()){
                
                String linea;
                    
                while((linea = breader.readLine()) !=null){
                    PCongelados.add(new ProductoCongelado(linea.split("รฐ")));
                }
                
                breader.close();
                reader.close();
            }
        }catch(IOException e){
        }
        try{
            
            File archivo = new File("D:/Escritorio/Proyecto-Final/src/archivos/productosFrescos.txt");
            FileReader reader = new FileReader(archivo);
            BufferedReader breader = new BufferedReader(reader);
            if(archivo.exists()){
                
                String linea;
                    
                while((linea = breader.readLine()) !=null){
                    PFrescos.add(new ProductoFresco(linea.split("รฐ")));
                }
                
                breader.close();
                reader.close();
            }
        }catch(IOException e){
            
        }
        try{
            
            File archivo = new File("D:/Escritorio/Proyecto-Final/src/archivos/productosRefrigerados.txt");
            FileReader reader = new FileReader(archivo);
            BufferedReader breader = new BufferedReader(reader);
            if(archivo.exists()){
                
                String linea;
                    
                while((linea = breader.readLine()) !=null){
                    PRefrigerados.add(new ProductoRefrigerado(linea.split("รฐ")));
                }
                
                breader.close();
                reader.close();
            }
        }catch(IOException e){
            
        }
        
        Productos.addAll(PCongelados);
        Productos.addAll(PFrescos);
        Productos.addAll(PRefrigerados);
        
        Collections.sort(Productos);
        
        Productos.forEach((Producto p) -> {
            mod.addRow(new Object[]{p.getNombre(),p.getFechaCaducidad(),p.getTipoDeProducto()});
        }); 
    }

